
package soalpertama;

public class Lingkaran {
  private int r1;
  private int x1;
  private int y1;
  private int r2;
  private int x2;
  private int y2;

    public Lingkaran(int r1, int x1, int y1, int r2, int x2, int y2) {
        this.r1 = r1;
        this.x1 = x1;
        this.y1 = y1;
        this.r2 = r2;
        this.x2 = x2;
        this.y2 = y2;
    }
    public Lingkaran (){
        
    }

    public int getR1() {
        return r1;
    }

    public void setR1(int r1) {
        this.r1 = r1;
    }

    public int getX1() {
        return x1;
    }

    public void setX1(int x1) {
        this.x1 = x1;
    }

    public int getY1() {
        return y1;
    }

    public void setY1(int y1) {
        this.y1 = y1;
    }

    public int getR2() {
        return r2;
    }

    public void setR2(int r2) {
        this.r2 = r2;
    }

    public int getX2() {
        return x2;
    }

    public void setX2(int x2) {
        this.x2 = x2;
    }

    public int getY2() {
        return y2;
    }

    public void setY2(int y2) {
        this.y2 = y2;
    }
  
  
  
  
}
